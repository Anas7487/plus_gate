function AGreen() {
    // Select the SVG element with ID "A"
    let svgElementA = document.getElementById("A");

    // Change the fill color of all circles inside the SVG with ID "A" to blue
    svgElementA.querySelectorAll("circle").forEach(circle => {
        circle.setAttribute("fill", "green");
    });
}

